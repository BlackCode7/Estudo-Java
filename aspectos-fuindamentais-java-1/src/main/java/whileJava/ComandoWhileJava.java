package whileJava;

public class ComandoWhileJava {

	public static void main(String[] args) {
		// Comando while em java
		
		int contador = 0;
		
		// while tem uma condição booleana
		while(contador < 11) {
			System.out.println("conatador >>> "+ contador);
			contador = contador + 1;
		}

	}

}
